        <h1>Doctors' money</h1>
        <p>Kenyan doctors have gone on innumerable strikes over salary woes, while their counterparts in Canada are unhappy about getting a pay rise. It is almost unheard of that a worker would complain of a high salary from their employer. If anything the search for better payment is one of the main reasons people are always hunting for new jobs. Over 700 doctors in Quebec, Canada have protested against their own pay rise.
        </p>
        <p>
          The doctors have asked the Government to spend the money on patient care and resources for their underpaid and overworked colleagues instead. In an open letter to the public published on February, the medical practitioners noted that, “The only thing that seems to be immune to cuts is our salary.” Part of the deal constitutes that Quebec’s 10,000 medical specialists will see their annual salary rise from $4.7 billion to $5.4 billion in 2023, an increase of 1.4 per cent each year.
        </p>
        <p>
          Data compiled by the Canadian Institute of Health Information shows that on average, specialists in the province earn $403,537 Canadian (Sh31.56m) annually. A physician is paid $339,000 (Sh26.52m) for clinical services by the government's Ministry of Health. A family physician earns $275,000 (Sh21.51m) for clinical services and a surgical specialist is paid $461,000 (Sh36.06m) per year on average.
        </p>
        <p>
          Last year, doctors in Kenya downed their tools for more than three months decrying poor facilities in hospitals, working conditions and allowances. “We deeply sympathise with the unfortunate plight of these highly esteemed professionals who have had to endure as they wait for their allowances,” Kenya Medical Practitioners, Pharmacists and Dentists Union (KMPDU) Secretary General Ouma Oluga said. They also complained that the Government did not honour the 2013 collective bargaining agreement (CBA) which would also see them earning better salaries and a have a better deployment system in place for the doctors. KMPDU signed an agreement with the Government in March 2017, ending the longest strike by doctors in Kenyan history.
        </p>       
