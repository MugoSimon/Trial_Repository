        <h1>Ebola Control</h1>
        <p>The Centers for Disease Control and Prevention (CDC) recommends the following preventive measures for people in an area affected by an outbreak:</p>
        <p>Practice careful hygiene (e.g., wash hands with soap and water, alcohol-based hand sanitizer, or chlorine solution)</p>
        <p>Avoid direct contact with bodies of people who have died from confirmed or suspected infection. During epidemics, direct contact with any dead body should be avoided</p>
        <p> Doffing: removal of used PPE is a high-risk process that requires a structured procedure, a trained observer, and a designated area for removal to ensure protection. PPE must be removed slowly and deliberately in the correct sequence to reduce the possibility of self-contamination or other exposure. A stepwise process should be developed and used during training and daily practice.</p>
        <p>If infection is suspected based on initial screening, immediate isolation is warranted before any further workup is carried out. This is crucial to reduce contact with other patients and healthcare workers while the patient is being investigated. Isolation measures should be continued until the patient has tested negative.</p>
        <p>Do not handle items that have come into contact with an infected person's body fluids (e.g., clothes, medical equipment, needles)</p>
        <p>Returning travelers (including healthcare workers) should follow local policies for surveillance and monitor their health for 21 days and seek medical attention if symptoms develop, especially fever.</p>
        <p>Avoid funeral or burial rituals that require handling of the body of someone who has died from confirmed or suspected Ebola virus infection</p>
