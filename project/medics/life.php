		<p>1. Follow DRSABCD. </p>
		<p>2. Rest the victim </p>
		<p>3. If they have no medication and are known to be allergic and suffering breathing difficulties an Asthma Spray may be used </p>
		<p>4. If they have an EPI pen (or another auto injector) assist to administer to medication to the thigh</p>
		<p>5. Stay with the patient until medical  aid arrives.</p>
