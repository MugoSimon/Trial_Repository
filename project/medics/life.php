	<div id="section2" class="bg-secondary text-white" style="padding-top:70px;padding-bottom:70px;">
		<h1>Shock</h1>
		<p>1. Follow DRSABCD. </p>
		<p>2. Help the patient to lie down. Do not raise their legs.</p>
		<p>3. Reassure the patient</p>
		<p>4. Manage severe bleeding then treat other injuries. </p>
		<p>5. Loosen any tight clothing</p>
		<p>6. Keep the patient warm with a blanket or similar. Do not use any source of direct heat. </p>
		<p>7. Give the patient small amounts of cool water to drink frequently if they are conscious, do not have abdominal trauma, and are unlikely to require an operation immediately. </p>
		<p>8. Place the patient in the recovery position if they have difficulty breathing, become unconscious or are likely to vomit. </p>
		<p>9. Seek medical aid or call triple zero (000) for an ambulance if the patient’s injuries require it.
</p>
	</div>
	<div id="section3" class="bg-danger" style="padding-top:70px;padding-bottom:70px;">
		<h1>Fracture</h1> 
		<p>1. Follow DRSABCD. </p>
		<p>2. Ask the patient to remain as still as possible. </p>
		<p>3. Control any bleeding, cover any wounds and check for other fractures. </p>
		<p>4. Immobilise the broken bone by placing a padded splint along the injured limb. </p>
		<p>5.Secure the splint by passing the bandages above and below the break to prevent movement. Tie the bandages firmly and away from the 	injury
		 </p>
		<p>6. For a leg fracture, also immobilise the foot and ankle. Support the limb while bandaging.  </p>
		<p>7. Check that the bandages are not too tight and watch for signs of loss of circulation to the limb every 15 minutes.</p>
		<p>8. Seek medical aid. </p>
	</div>
	<div id="section4" class="bg-info" style="padding-top:70px;padding-bottom:70px;">
		<h1>Snake bite</h1>
		<p>1. Follow DRSABCD. </p>
		<p>2. Call triple zero (000) for an ambulance. </p>
		<p>3. Lie the patient down and ask them to keep still.  Reassure the patient.  </p>
		<p>4. If on a limb, apply an elasticised roller bandage (10–15 cm wide)  over the bite site as soon as possible.</p>
		<p>5Apply a further elasticised roller bandage (10–15 cm wide), starting just above the fingers or toes and moving upwards on the bitten limb as far as  can be reached. 
		 </p>
		<p>6.Use clothing or other material if an elasticised roller bandage is not available. 
		Apply the bandage as tightly as possible to the limb.   </p>
		<p>7. Immobilise the bandaged limb  using splints. </p>
		<p>8. Write down the time of the bite and when the bandage was applied. If possible, mark the location of the bite site (if known) on the skin with a pen, or photograph the site.  </p>
		<p>Stay with the patient until medical  aid arrives.
		</p>
	</div>
	<div id="section5" class="bg-dark text-white" style="padding-top:70px;padding-bottom:70px;">
		<h1>Asthma emergency</h1>
		<p>1. Follow DRSABCD. </p>
		<p>2. Help the patient to sit down in a comfortable position. </p>
		<p>3. Reassure and stay with the patient. </p>
		<p>4. If requested, help the patient to follow their action plan </p>
		<p><b> HOW TO GIVE MEDICATION (4 : 4 : 4) </b> </p>
		<p> Use a spacer if available </p>
		<p>1. Give 4 separate puffs of blue/grey reliever puffer: </p>
		<ul>
			<li>shake the inhaler </li>
			<li>give 1 puff </li>
			<li>take 4 breaths</li>
			<li>repeat until 4 puffs have been given. </li>
		</ul>
		<p>2.  Wait 4 minutes  </p>
		<p>3. If there is no improvement, give 4 more separate puffs of blue/grey reliever as above. </p>
		<p>4.If the patient still cannot breath normally, call triple zero (000) for an ambulance. </p>
		<p>Keep giving 4 puffs every 4 minutes (as above) until medical aid arrives. </p>
	</div>
