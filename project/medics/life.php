		<h1>Burns</h1>
		<p>1. Follow DRSABCD. </p>
		<p>2. General – Running water for 10 minutes, </p>
		<p>3. Moulton material 20 minutes.</p>
		<p>4. Remove clothing except clothing stuck to the body</p>
		<p>5. Remove watches and jewellery</p>
		<p>6.  Elevate injured area</p>
		<p>7. After cooling cover with sterile non-stick bandage </p>
