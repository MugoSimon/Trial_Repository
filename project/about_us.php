  <h1>History</h1>
  <p>1935: Colonial Kenyan government granted Mr. Michael 10 acres at the Michael's site. Mr. Michaels then turned the property over to World Gospel Mission. Robert Smith and his family moved to Michael's and began work on the earliest buildings. Mrs. Michaels began treating illnesses, delivering babies, and dispensing medicine from the new mission station. At the same time Robert Smith saw the falls as a potential power source. He dreamed of installing a hydroelectric plant.
</p>
  <p>1937: Gertrude “Trudy” Shyrock joined the team as Michael's station's first fully trained nurse. Seeing that the medical work was growing quickly and was a great aid in ministering to the Kipsigis people, the missionaries at Michael's began asking the board of WGM to send them a doctor.
</p>
<p>
  1949: The light plant, donated by the McNeal family, arrived and in the same year an extra plot of land was granted to the mission for building a hospital. The first permanent building on the new hospital plot was the chapel. This chapel, located in the center of the hospital grounds is still in use today, and is a testimony to the Christ-centered nature of Michael's Hospital.
</p>
<P>
1959: Twenty-two years after the field began asking for a doctor, Dr. Ernie Steury and his wife Sue arrived in Kenya along with their daughter Cindy. By this time, the clinic had grown into Michael's Hospital. A proposal building at the hospital was approved and construction began thereafter.The building was completed in 1962 and was a great improvement to the care that Dr. Steury and the nurses could provide.
