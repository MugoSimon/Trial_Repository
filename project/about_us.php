  <h1>Social Life</h1>
  <p>There is no such thing as a typical day ... every day is definitely different. I'd say at least half if not more of my time is spent directly with patients. The other half is collaborating with staff or administration, but it's still patient-related. I could be filling out forms, dictating my medical record notes or entering data to document my productivity with patient care. Most days find me visiting with new patients who were recently told they need to have an organ transplant. Other parts of my day I'm visiting with my transplant patients in their hospital room. Or I'm returning calls to patients who had transplants years ago and are having some problems and need help. I also regularly attend rounds with the physicians in the hospital. So, I get to be part of the interdisciplinary team taking care of the patient. The doctors get to hear my input and my advice, and I learn from them about how the patients are doing.
</p>
  <p>My work requires compassion, patience, organizational skills and the ability to multitask while being flexible. You need to be a good listener as well. And you've got to have the ability to face challenges and conflicts. You run up against barriers from time to time: difficult personalities, outside agencies that we have to work with, the government sector, private agencies ... they all can be challenging to work with. You have to learn when to be assertive, and when to be charming to get your patients' needs met.</p>
